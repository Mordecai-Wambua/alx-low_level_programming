#include "3-calc.h"

/**
 * op_add - addition function
 * @a:first number
 * @b:second number
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtraction function
 * @a:first number
 * @b:second number
 *
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplication function
 * @a:first number
 * @b:second number
 *
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division function
 * @a:first number
 * @b:second number
 *
 * Return: division result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus function
 * @a:first number
 * @b:second number
 *
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
